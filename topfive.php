9.เลิกเคี้ยวหมาก<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	มะเร็งหลอดอาหาร<br>
<strong>พฤติกรรมเสี่ยง</strong><br>
1.อายุ ผู้ที่มีอายุระหว่าง 45-70 ปีมีความเสี่ยงสูงสุด<br>
2.การสูบบุหรี่ ความเสี่ยงจะเพิ่มมากขึ้นตามปริมาณและระยะเวลาที่สูบบุหรี่<br>
3.การดื่มแอลกอฮอล์ เพิ่มความเสี่ยงของการเป็นโรคมะเร็งหลอดอาหารโดยเฉพาะชนิด squamous cell<br>
4.ภาวะของหลอดอาหารที่เรียกว่า Barrett’s esophagus <br>ซึ่งเป็นภาวะที่เกิดในผู้ที่มีการอักเสบของหลอดอาหารจากกรดในกระเพาะอาหารทำลายเซลล์บุผนังหลอดอาหาร ทำให้เกิดมะเร็งชนิด adenocarcinoma ได้<br>
5.การรับประทานอาหารที่มีผัก ผลไม้ และแร่ธาตุต่ำ<br>
6.ภาวะอ้วน<br>
<br><br>
<strong>อาการ</strong><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;มะเร็งหลอดอาหารในระยะเริ่มต้นมักไม่ค่อยก่อให้เกิดอาการ แต่เมื่อเป็นมากขึ้นอาจมีอาการแสดงดังนี้<br>
1.กลืนอาหารลำบาก โดยเฉพาะเมื่อรับประทานอาหารแข็ง เช่น เนื้อสัตว์ ขนมปัง หรือผักสด แต่เมื่อโรคลุกลามขึ้นจนก้อนมะเร็งไปอุดตันทางเดินอาหารก็จะทำให้เกิดอาการเจ็บ แม้แต่การดื่มน้ำ<br>
2.แน่นหน้าอกหรือแสบร้อนในช่องอก<br>
3.รู้สึกท้องอืด อาหารไม่ย่อย<br>
4.คลื่นไส้<br>
5.สำลักหลังกลืนอาหาร<br>
6.น้ำหนักตัวลดลงโดยไม่ทราบสาเหตุ<br>
7.ไอ<br>
8.เจ็บบริเวณกระดูกหน้าอกหรือในลำคอ<br>
<br><br>
<strong>การวินิจฉัย</strong><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	การซักประวัติและการตรวจร่างกาย<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	การกลืนแป้งสารทึบแสง เป็นวิธีการตรวจโดยให้ผู้ป่วยกลืนน้ำที่ผสมด้วยสารทึบแสงคล้ายแป้ง ซึ่งจะไปเคลือบหลอดอาหาร กระเพาะอาหาร และลำไส้เล็ก และทำการถ่ายภาพเอกซเรย์เป็นระยะๆ ทำให้สามารถมองเห็นก้อนเนื้อหรือความผิดปกติอื่นๆ ได้<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	การส่องกล้องระบบทางเดินอาหารส่วนต้น หากตรวจพบสิ่งผิดปกติหรือสิ่งที่น่าสงสัยสามารถตัดชิ้นเนื้อเพื่อนำไปตรวจสอบต่อไปได้<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	การตัดชิ้นเนื้อตรวจทางพยาธิวิทยา ซึ่งมักจะทำระหว่างการส่องกล้องทางเดินอาหาร<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	การตรวจเอกซเรย์คอมพิวเตอร์ (CT scan) ซึ่งจะแสดงภาพอวัยวะภายในแบบสามมิติ <br>ช่วยให้แพทย์สามารถเห็นตำแหน่งของโรคและการกระจายของโรคได้ละเอียดมากกว่าการเอกซเรย์ธรรมดา
การรักษา<br>
จะอาศัยทีมแพทย์ในสาขาต่างๆ เช่น ศัลยแพทย์ รังสีแพทย์ และอายุรแพทย์ผู้เชี่ยวชาญด้านโรคมะเร็ง<br> มาร่วมกันวางแผนการรักษาให้เหมาะสมสำหรับผู้ป่วยแต่ละราย โดยปัจจัยที่มีผลต่อการเลือกวิธีการรักษาของแพทย์ เช่น<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	ขนาด ตำแหน่ง และลักษณะของเซลล์มะเร็ง<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	ระยะโรคและการกระจายของมะเร็ง<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	สุขภาพโดยรวมของผู้ป่วย<br>
<br><br>
<strong>การป้องกัน</strong><br>
1.	งดการสูบบุหรี่ ดื่มแอลกอฮอล์<br>
2.	ควบคุมน้ำหนักให้อยู่ในเกณฑ์มาตรฐานตลอดเวลา<br>
3.	เลือกทานอาหารที่มีประโยชน์ต่อร่างกาย เพิ่มการทานผักผลไม้ให้มากขึ้น<br>
4.	รักษาโรคกรดไหลย้อน หรือภาวะหลอดอาหารอักเสบเรื้อรังให้อาการดีขึ้นโดยเร็วที่สุด<br>
<br><br>
เพศหญิง<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	มะเร็งเต้านม<br>
พฤติกรรมเสี่ยง คนที่ชอบอาหารที่มีไขมันสูง ชอบกินอาหารขยะ อาหารฟาสต์ฟู้ด หรือชอบสูบบุหรี่ ดื่มแอลกอฮอล์เป็นประจำ และขาดการออกกำลังกายอย่างสม่ำเสมอ<br>
<strong>อาการ</strong><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ผู้ป่วยส่วนใหญ่มาพบแพทย์เมื่อ พบก้อนในเต้านม<br>
<strong>การวินิจฉัย</strong><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;หลังจากแพทย์ตรวจพบก้อนแล้ว จะพิจารณาทำการตรวจเพิ่ม ดังนี้<br>
1.X-ray เต้านม และอัลตราซาวด์<br>
2.ใช้เข็มเจาะก้อนเพื่อดูเซลล์ การใช้เข็มเจาะตัดเนื้อจากก้อน<br>
3.การผ่าตัดเพื่อนำก้อนออก<br>
<strong>การรักษา</strong><br>
1.การผ่าตัด <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.1.ผ่าตัดแบบสงวนเต้านมไว้ และเลาะต่อมน้ำเหลืองที่รักแร้ออก<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1.2.ผ่าตัดแบบตัดเต้านม ออกทั้งหมดและเลาะต่อมน้ำเหลืองที่รักแร้ออก<br>
2.การฉายรังสี<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2.1.หากผ่าตัด แบบสงวนเต้านมไว้ มีความจำเป็นต้องฉายรังสีต่อ<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2.2.หากผ่าตัดแบบตัดเต้านมออกทั้งหมด และเลาะต่อมน้ำเหลืองที่รักแร้ออกจะฉายรังสีหลังผ่าตัด ในกรณีก้อนมะเร็งมีขนาดใหญ่<br> มีการกระจายของเซลล์มะเร็ง<br>
3.การใช้ยาฮอร์โมนและเคมีบำบัด โดยทั่วไปหากก้อนมะเร็งโตกว่า 1 ชม. มะเร็งแพร่กระจายไปต่อมน้ำเหลืองแล้ว<br>
<strong>การป้องกัน</strong><br>
1.สตรีทุกท่านควรตรวจเต้านมของตนเองเป็นประจำทุกเดือน ควรได้รับการตรวจจากแพทย์หรือพยาบาล เป็นครั้งคราวเพื่อจะได้รับการวินิจฉัยและการรักษา<br>
2.ในผู้ที่เป็นกลุ่มเสี่ยงควรได้รับการตรวจ X-ray เต้านม (Mammogram) ปีละ 1 ครั้ง<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	มะเร็งปากมดลูก<br>
<strong>พฤติกรรมเสี่ยง</strong> ผู้ที่มีเพศสัมพันธ์ต่ำกว่า 18 ปี มีพฤติกรรมเปลี่ยนคู่นอนบ่อย รวมทั้งมีพฤติกรรมทางเพศที่ไม่เหมาะสม และผู้ที่มีภูมิคุ้มกันต่ำหรือบกพร่อง สตรีที่สูบบุหรี่หรือผู้ใกล้ชิดเป็นผู้สูบบุหรี่<br>
<strong>อาการ</strong> โดยทั่วไปในระยะแรก จะไม่ปรากฏอาการใดๆ แต่ในระยะต่อมาผู้ป่วยจะพบ<br>
1.ตกขาวหรือมีเลือดออก ผิดปกติทางช่องคลอด<br>
2.ตรวจพบแผลหรือก้อนที่ปากมดลูก<br>
การวินิจฉัย<br>
1.การตรวจ Pap smear<br>
2.การตรวจชิ้นเนื้อ<br>
<strong>การรักษา</strong><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ผู้ป่วยมะเร็งปากมดลูกอาจรักษาให้หายขาดได้เมื่อตรวจพบ โรคระยะแรกวิธีการรักษาขึ้นอยู่กับระยะของโรค โดยแพทย์จะพิจารณาเป็นรายไป ได้แก่<br>
1.การผ่าตัด<br>
2.การใส่แร่<br>
3.การฉายแสง<br>
4.เคมีบำบัด<br>
<strong>การป้องกัน</strong><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;สามารถป้องกันได้โดย<br>
1.การปรับเปลี่ยนพฤติกรรมที่เสี่ยง<br>
2.ค้นหาการติดเชื้อเอชพีวี และมะเร็งปากมดลูกโดยการตรวจ Pap smear<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	มะเร็งรังไข่<br>
<strong>พฤติกรรมเสี่ยง</strong> <br>
1.สภาพแวดล้อม เช่น สารเคมี อาหาร เนื่องจากพบว่าในประเทศ อุตสาหกรรมมีผู้ป่วยเป็น มะเร็งรังไข่มากกว่าประเทศเกษตรกรรม<br>
2.สตรีที่ไม่ มีบุตร หรือมีบุตรน้อย<br>
3.ผู้ที่เคย เป็นมะเร็งที่เต้านม มะเร็ง มดลูก และมะเร็งระบบทางเดิน อาหาร โอกาสเป็น มะเร็งรังไข่มี มากกว่าคนปกติ<br>

<strong>อาการ</strong><br>
1. อาจไม่มีอาการ แพทย์ตรวจพบโดยบังเอิญ<br>
2. มีอาการท้องอืดเป็นประจำ<br>
3. มีก้อนในท้องน้อย<br>
4. ปวด แน่นท้อง และถ้าก้อนมะเร็งโตมากจะกด กระเพาะปัสสาวะหรือลำไส้ส่วนปลาย ทำให้ ถ่ายปัสสาวะหรืออุจจาระลำบาก<br>
5. ในระยะท้ายๆอาจมีน้ำในช่องท้องทำให้ท้องโต ขึ้นกว่าเดิม เบื่ออาหาร ผอมแห้ง น้ำหนักลด<br>
<strong>การวินิจฉัย</strong><br>
1. การตรวจภายในอาจคลำพบก้อนใน บริเวณท้องน้อย การคลำพบก้อนรังไข่ได้ในสตรีวัย หมดประจำเดือน ควรนึกถึงมะเร็งของรังไข่ไว้ด้วย (เพราะตามปกติวัยหมดประจำเดือน รังไข่จะฝ่อ)<br>
2. การทำแพพสเมียร์จากในช่องคลอด ส่วนบนทางด้านหลัง อาจพบเซลล์มะเร็งของรังไข่ ได้<br>
3.การตรวจด้วย เครื่องความถี่สูงอาจช่วยบอกได้ว่ามีก้อน ในท้อง ในรายที่อ้วนหรือหน้าท้อง หนามาก คลำด้วยมือตาม ปกติตรวจไม่พบ<br>
4. การผ่าตัดเปิดช่องท้อง และตรวจดู เป็นวิธีที่สำคัญ และแม่นยำที่สุดในการวินิจฉัยโรค อย่างแน่นอนสามารถ ขลิบหรือตัดเอาเนื้อมาตรวจหาชนิดของมะเร็ง และทราบถึงระยะ ของโรค ด้วย<br>
<strong>การรักษา</strong><br>
 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;การผ่าตัดเป็นวิธีแรกที่แพทย์จะเลือกทำการ รักษา ถ้าไม่สามารถตัดออกได้หมดเนื่องจาก โรคกระจายออกไปมากแล้ว แพทย์จะพยายาม ตัดออกให้ได้มากที่สุดเท่าที่จะทำได้ แล้วจะให้ การรักษาต่อด้วยเคมีบำบัด หรือรังสีบำบัด<br>
 <strong>การป้องกัน</strong><br>
 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;เนื่องจาก มะเร็งรังไข่ในระยะแรก ๆ มักจะไม่มีอาการ อีกทั้งยังไม่ทราบสาเหตุที่ แท้จริง การป้องกันจึงทำได้ยาก ดังนั้นวิธีที่ดีที่สุดคือ รับการตรวจภายในหรือตรวจด้วยคลื่นความ ถี่สูง โดยแพทย์อย่างน้อยปีละครั้ง<br>
            
