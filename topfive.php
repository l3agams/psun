&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	มะเร็งลำไส้ใหญ่และทวารหนัก<br>
<strong>พฤติกรรมเสี่ยง</strong> ผู้ที่ชอบทานอาหารประเภทเนื้อสัตว์ปิ้งย่างไหม้เกรียม สุกๆ ดิบๆ อาหารหมักดอง อาหารไขมันสูง สูบบุหรี่ ดื่มเครื่องดื่มประเภทแอลกอฮอล์ อย่างต่อเนื่องเป็นเวลานาน ไม่ทานผักผลไม้ และไม่ออกกำลังกาย<br>
<strong>อาการ</strong> ผู้ป่วยระยะแรกอาจไม่มีอาการใดๆ หรืออาจมีอาการปวดท้อง แน่นท้องคล้ายโรคกระเพาะ เมื่อโรคเป็นมากขึ้นอาจมีอาการท้องผูกสลับท้องเดิน มีลักษณะในการขับถ่ายที่เปลี่ยนไป อาจีการอุดตันของลำไส้ ถ่ายอุจจาระเป็นเลือดหรือมูกปนเลือด หรือมีเลือดออกในอุจจาระเป็นเวลานาน ทำให้มีภาวะซีดได้<br>
<strong>การวินิจฉัย</strong> เนื่องจากในระยะแรก ผู้ป่วยมักจะไม่มีอาการ การตรวจเลือดในอุจจาระและการส่องกล้องตรวจลำไส้ใหญ่ อาจจะทำให้ตรวจพบมะเร็งได้ตั้งแต่ระยะเริ่มต้นซึ่งจะทำให้มีโอกาสรักษา ให้หายขาดได้แนะนำให้เริ่มตรวจด้วย วิธีดังกล่าวตั้งแต่อายุ 50 ขึ้นไป<br>
<strong>การรักษา</strong> กรณีที่มะเร็งยังอยู่เฉพาะที่ลำไส้ใหญ่และต่อมน้ำเหลืองบริเวณลำไส้ จะรักษาโดยการผ่าตัดเอาลำไส้ ส่วนที่เป็นมะเร็งและต่อมน้ำเหลืองออก ส่วนมะเร็งที่เกิดบริเวณทวารหนัก จะใช้การฉายแสงร่วม กรณีมะเร็งแพร่กระจาย ไปยังต่อมน้ำเหลือง การใช้เคมีบำบัดหลังการผ่าตัดหรือฉายแสง จะช่วยเพิ่มอัตราการหายขาดจากโรคได้ ซึ่งโดยทั่วไปผู้ป่วยจะได้รับยาเคมีบำบัดเป็นระยะเวลา 6 เดือน<br>
<strong>การป้องกัน</strong> การรับประทานอาหารที่มีเส้นใยมาก เช่น ผัก ผลไม้ และอาหารที่มีแคลเซี่ยมสูง อาจช่วยป้องกันการเกิดมะเร็งชนิดนี้ได้<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	มะเร็งหลอดลมและปอด<br>
<strong>พฤติกรรมเสี่ยง</strong> การสูบบุหรี่ด้วยตัวเองหรือการสูดดมควันบุหรี่มือสอง การสูดดมฝุ่น รวมถึงมลพิษในอากาศเป็นระยะเวลายาวนาน
<strong>อาการ</strong> ในระยะแรก ไม่มีอาการผิดปกติอะไรเลย เมื่อก้อนมะเร็งโตขึ้นจนไปกดหลอดลมหรือลุกลามไปสู่อวัยวะอื่น จะมีอาการไอมีเลือดออก เจ็บหน้าอก หอบเหนื่อย มีหน้าบวม แขนบวม เจ็บหน้าอกและหัวไหล่ น้ำหนักลด เบื่ออาหาร<br>
<strong>การวินิจฉัย</strong> <br>
1.ซักประวัติตรวจร่างกาย<br>
2.X-ray ปอด<br>
3.ตรวจชิ้นเนื้อจากปอด<br>
4.ตรวจเสมหะ<br>

<strong>การรักษา</strong> <br>
1.การผ่าตัด ทำในกรณีที่ก้อนขนาดไม่ใหญ่มาก<br>
2.การฉายแสงร่วมกับการให้เคมีบำบัด เพื่อช่วยยับยั้งชะลอการลุกลาม<br>
3.ประคับประคองตามอาการ<br>
<strong>การป้องกัน</strong> มะเร็งปอดเป็นโรคร้ายที่พบบ่อยและเมื่อเกิดขึ้นแล้ว สามารถรักษาให้หายขาดได้น้อย การป้องกันโดยการไม่สูบบุหรี่ หรือหากสูบบุหรี่อยู่ก็ควรเลิกหรือลดให้เร็วที่สุด ซึ่งจะช่วยลดความเสี่ยงลงได้มาก<br> 
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	มะเร็งตับและท่อน้ำดี<br>
<strong>พฤติกรรมเสี่ยง</strong> ผู้ที่ชอบทานอาหารสุกๆ ดิบๆ ไหม้เกรียม หรืออาหารที่มีเชื้อรา อาหารที่ใส่สารกันบูด ที่สำคัญคือ ผู้ที่ชอบเครื่องดื่มที่มีแอลกอฮอล์ทุกประเภท<br>
<strong>อาการ</strong><br>
1.เริ่มต้นด้วยอาการเบื่ออาหาร แน่นท้อง ท้องผูก<br>
2.อ่อนเพลีย น้ำหนักลด และมีไข้ต่ำ<br>
3.ปวดหรือเสียดชายโครง ด้านขวา อาจคลำก้อนได้<br>
4.ตัวเหลือง ตาเหลือง ท้องโตและบวมบริเวณขาทั้งสองข้าง<br>
<strong>การวินิจฉัย</strong><br> 
1.การตรวจหาระดับแอลฟาฟีโตโปรตีนในเลือด<br>
2.การใช้เครื่องมือพิเศษ ได้แก่ เครื่องอัลตราซาวน์ เครื่องแสดงภาพอวัยวะด้วยรังสีไอโซโทป และเครื่องเอกซเรย์คอมพิวเตอร์<br>
3.การดูลักษณะของเซลล์ ด้วยการเจาะเอาเนื้อตับมาตรวจ<br>
